def normalize_pos(pos):
    """Normalize POS tags for matching. Replace fullwidth characters with ASCII."""
    pos = pos.strip()
    pos = pos.replace('；', ';')  # fullwidth semicolon ; -> ASCII ;
    pos = pos.replace('．', '.')   # fullwidth period ． -> ASCII .
    return pos

