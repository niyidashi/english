batch_dir = 'C:/Users/YCT/Desktop/单词/batches'
files = ['batch_04.json', 'batch_05.json', 'batch_06.json', 'batch_07.json', 'batch_08.json']

total = 0
found = 0
missing = []

for fname in files:
    path = os.path.join(batch_dir, fname)
    with open(path, 'r', encoding='utf-8') as f:
        batch = json.load(f)

    for w in batch:
        total += 1
        en = w['en'].strip().lower()
        pos = normalize_pos(w['pos'])
        key = (en, pos)

        if key in ZH:
            w['zh'] = ZH[key]
            found += 1
        else:
            missing.append(f"  id={w['id']} en=\"{w['en']}\" pos=\"{w['pos']}\"")

    with open(path, 'w', encoding='utf-8') as f:
        json.dump(batch, f, ensure_ascii=False, indent=2)

    print(f"Updated {fname}: {len(batch)} words")

print(f"\nTotal: {total} words, {found} translated, {len(missing)} missing")
